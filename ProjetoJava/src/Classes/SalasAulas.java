public class SalasAulas {

    private int id_sala;
    private int numero_sala;
    private String curso_sala;
    private int capacidade_sala;


    public int getId_sala() {
        return id_sala;
    }

    public void setId_sala(int id_sala) {
        this.id_sala = id_sala;
    }

   



    public String getCurso_sala() {
        return curso_sala;
    }

    public void setCurso_sala(String curso_sala) {
        this.curso_sala = curso_sala;
    }

    public int getNumero_sala() {
        return numero_sala;
    }

    public void setNumero_sala(int numero_sala) {
        this.numero_sala = numero_sala;
    }

    public int getCapacidade_sala() {
        return capacidade_sala;
    }

    public void setCapacidade_sala(int capacidade_sala) {
        this.capacidade_sala = capacidade_sala;
    }

  

    

    

}
